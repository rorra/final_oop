public class Liquidacion {
	private Empleado empleado;
	private Fecha fecha;
	
	public Empleado getEmpleado() {
		return empleado;
	}
	
	public void setEmpleado(Empleado empleado) {
		this.empleado = empleado;
	}
	
	public Fecha getFecha() {
		return fecha;
	}
	
	public void setFecha(Fecha fecha) {
		this.fecha = fecha;
	}
	
	
}
